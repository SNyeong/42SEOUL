#include "libft.h"

char	*ft_itoa(int n)
{
	char	*ret;
	char	ch;
	int		i;

	if (n < 0)
	{
		write(1, '-', 1);
		i = n / 10;
		ch = '0' - i;
		write(1, ch, 1);
	}
	return (ret);
}
